# inteiro para float
preco = 10
print(preco)

preco = float(preco)
print(preco)

preco = 10 / 2
print(preco)

#float para inteiro
preco = 10.30
print(preco)

preco = int(preco)
print(preco)

# número para string
preco = 10.5
idade = 28
texto = f"idade {idade} preço {preco}"
print(texto)

# string para número
preco = "10.5"
idade = 28
print(float(preco))
print(int(idade))
